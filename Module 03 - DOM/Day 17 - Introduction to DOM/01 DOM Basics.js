//01. DOM: Basically an AST of the HTML that we can read & write via JS. It's an object that has a lot of objects inside it. More like a folder with lots of sub-folders (& ultimately files) inside.

//Some important objects & keywords in DOM:
    //a) Document: This is the root object. it's "URL" property is is the page link.
    //b) Node: Anything & everything in DOM is a node (folder/file).
    //c) Element: A certain kind of node (folder).
    //d) NodeList: An array-like of nodes.
    //e) Attribute: The adjectives for a noun (element).
    //f) NameNodeMap: An unordered collection of attributes.