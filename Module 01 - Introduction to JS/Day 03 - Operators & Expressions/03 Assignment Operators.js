    let i = 0;

//02. shorthand with arithmetic operators:
    //a) add:
        i += 11; // 0+11 = 11
        console.log(i);
    //b) subtract:
        i -= 1; // 11-1 = 10
        console.log(i);
    //c) multiply:
        i *= 5; // 10*5 = 50
        console.log(i);
    //d) divide:
        i /= 10; // 50/10 = 05
        console.log(i);
    //e) remainder:
        i %= 3; // 05%03 = 02
        console.log(i);
    //f) exponentiation:
        i **= 10; // 02^10 = 1024
        console.log(i);